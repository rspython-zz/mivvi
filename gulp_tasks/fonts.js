// Load gulp module

var gulp = require('gulp');
var config = require('../config');

// Fonts
//--------------------------------

gulp.task('fonts', function () {
    return gulp.src([
            config.paths.bower + '/font-awesome/fonts/**',
            config.paths.assets.fonts + '/**/*'
        ])
        .pipe(gulp.dest(config.paths.dist + '/fonts'));
});
