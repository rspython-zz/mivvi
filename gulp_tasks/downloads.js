// Load gulp module

var gulp = require('gulp');
var config = require('../config');

// Images
//--------------------------------

gulp.task('downloads', function () {
    return gulp.src([
            config.paths.assets.downloads + '/**/*'
        ])
        .pipe(gulp.dest(config.paths.dist + '/downloads'));

});